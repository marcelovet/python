# Tipos int e float
# int -> número inteiro
# tipo int representa qualquer número, positivo ou negativo
print(11, -11)
print(0)

# float -> número com ponto flutuante
# representa qualquer número negativo ou positivo com casa decimal
print(11.4, -11.4)

# A classe type mostra o tipo que o python inferiu ao valor
print(11, "é", type(11))
print(-11, "é", type(-11))
print(11.1, "é", type(11.1))
