"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
Dinâmica - ele compreende o tipo de dado sem precisar declarar explicitamente
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print('Uma string')
print('Outra "string"')

# Aspas duplas
print("Outra string")

# Escape
print("Outra \"string\"")

# r - utilizado para expressões regulares
print(r"Outra \"string\"")
